def generate_observation(best_reward, final_reward, episode_length):

    # Handle missing values
    if final_reward == "N/A":
        return "Training completed, but final reward could not be extracted."

    observation = []

    # -------------------------
    # Reward Quality
    # -------------------------

    if best_reward >= 15:
        observation.append(
            "Excellent performance. The agent consistently wins games."
        )

    elif best_reward >= 5:
        observation.append(
            "Good performance. The agent learned a competitive strategy."
        )

    elif best_reward >= -5:
        observation.append(
            "Moderate learning. The agent is beginning to play effectively."
        )

    elif best_reward >= -15:
        observation.append(
            "Some learning occurred, but the policy is still inconsistent."
        )

    else:
        observation.append(
            "Poor performance. The agent is still struggling to learn."
        )

    # -------------------------
    # Compare final vs best
    # -------------------------

    if final_reward != "N/A":

        if final_reward >= best_reward - 1:
            observation.append(
                "Training remained stable until completion."
            )

        else:
            observation.append(
                "Performance declined after reaching the best evaluation score."
            )

    # -------------------------
    # Episode Length
    # -------------------------

    if episode_length != "N/A":

        if episode_length > 700:
            observation.append(
                "Long episodes indicate the agent survives longer."
            )

        elif episode_length < 250:
            observation.append(
                "Short episodes suggest frequent losses."
            )

    return " ".join(observation)

