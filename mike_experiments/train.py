from stable_baselines3 import DQN
from stable_baselines3.common.callbacks import EvalCallback
import os
import time


def run_experiment(
    exp_num,
    policy="CnnPolicy",
    lr=1e-4,
    gamma=0.99,
    batch=32,
    eps_final=0.05,
    timesteps=500000,
    continue_training=False,
):

    print("\n")
    print("=" * 80)
    print(f"STARTING EXPERIMENT {exp_num}")
    print("=" * 80)

    # ----------------------------------------------------
    # Create folders
    # ----------------------------------------------------

    model_dir = f"{PROJECT}/models/experiment_{exp_num}"
    log_dir = f"{PROJECT}/logs/experiment_{exp_num}"

    os.makedirs(model_dir, exist_ok=True)
    os.makedirs(log_dir, exist_ok=True)

    model_path = os.path.join(model_dir, "experiment_model")

    # ----------------------------------------------------
    # Evaluation Callback
    # ----------------------------------------------------

    callback = EvalCallback(

        eval_env,

        best_model_save_path=model_dir,

        log_path=log_dir,

        eval_freq=10000,

        deterministic=True,

        render=False,

    )

    # ----------------------------------------------------
    # Load or Create Model
    # ----------------------------------------------------

    if continue_training and os.path.exists(model_path + ".zip"):

        print("Loading existing model...")

        model = DQN.load(

            model_path,

            env=train_env,

            device="cuda",

        )

    else:

        print("Creating new model...")

        model = DQN(

            policy=policy,

            env=train_env,

            learning_rate=lr,

            gamma=gamma,

            batch_size=batch,

            buffer_size=100000,

            learning_starts=50000,

            target_update_interval=1000,

            train_freq=4,

            exploration_initial_eps=1.0,

            exploration_final_eps=eps_final,

            exploration_fraction=0.1,

            tensorboard_log=log_dir,

            verbose=1,

            device="cuda",

        )

    # ----------------------------------------------------
    # Train
    # ----------------------------------------------------

    print("\nTraining started...\n")

    start_time = time.time()

    model.learn(

        total_timesteps=timesteps,

        callback=callback,

        reset_num_timesteps=not continue_training,

    )

    elapsed = (time.time() - start_time) / 60

    # ----------------------------------------------------
    # Save Model
    # ----------------------------------------------------

    model.save(model_path)

    # ----------------------------------------------------
    # Extract Metrics
    # ----------------------------------------------------

    best_reward = callback.best_mean_reward

    logger = model.logger.name_to_value

    final_reward = logger.get("rollout/ep_rew_mean", "N/A")

    final_length = logger.get("rollout/ep_len_mean", "N/A")

    total_steps = model.num_timesteps

    # ----------------------------------------------------
    # Generate Observation
    # ----------------------------------------------------

    observation = generate_observation(

        best_reward,

        final_reward,

        final_length,

    )

    # ----------------------------------------------------
    # Print Summary
    # ----------------------------------------------------

    print_summary(

        exp_num=exp_num,

        policy=policy,

        lr=lr,

        gamma=gamma,

        batch=batch,

        eps_final=eps_final,

        best_reward=best_reward,

        final_reward=final_reward,

        episode_length=final_length,

        elapsed=elapsed,

        timesteps=total_steps,

        model_path=model_path,

        log_path=log_dir,

        observation=observation,

    )

    # ----------------------------------------------------
    # Save Results
    # ----------------------------------------------------

    save_results(

        exp_num=exp_num,

        policy=policy,

        lr=lr,

        gamma=gamma,

        batch=batch,

        eps_final=eps_final,

        best_reward=best_reward,

        final_reward=final_reward,

        episode_length=final_length,

        training_time=elapsed,

        timesteps=total_steps,

        model_path=model_path,

        observation=observation,

    )

    print("\nExperiment completed successfully.")

    return model

